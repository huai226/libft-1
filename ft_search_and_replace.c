#include "libft.h"

void	ft_search_and_replace(char *s, char c1, char c2)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c1)
			write(1, &c2, 1);
		else
			write(1, &s[i], 1);
		i++;
	}
}
