#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	if (n == 0 || s1 == s2)
		return (s1);
	while (n > 0)
	{
		((char *)s1)[n - 1] = ((char *)s2)[n - 1];
		n--;
	}
	return (s1);
}
