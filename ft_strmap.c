#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int i;
	char *new;

	i = 0;
	if (!s)
		return (NULL);
	new = ft_strnew(ft_strlen(s));
	if (!new)
		return (NULL);
	while (s[i])
	{
		new[i] = f(s[i]);
		i++;
	}
	return (new);
}
